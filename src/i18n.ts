function detectLocale(): Locale {
  const supported: Locale[] = ['en', 'ko', 'ja', 'es', 'pt', 'zh-CN', 'zh-TW', 'de', 'fr'];
  for (const lang of navigator.languages) {
    const exact = lang as Locale;
    if (supported.includes(exact)) return exact;
    // zh-Hans → zh-CN, zh-Hant → zh-TW
    if (lang.startsWith('zh-Hans') || lang === 'zh') return 'zh-CN';
    if (lang.startsWith('zh-Hant')) return 'zh-TW';
    const prefix = lang.split('-')[0] as Locale;
    if (supported.includes(prefix)) return prefix;
  }
  return 'en';
}

let currentLocale: Locale = detectLocale();

export function getLocale(): Locale {
  return currentLocale;
}

export function setLocale(locale: Locale) {
  currentLocale = locale;
}

export function t(key: string): string {
  return messages[currentLocale]?.[key] ?? messages.en[key] ?? key;
}

export function getLocaleNames(): Record<Locale, string> {
  return LOCALE_NAMES;
}

export function getHtmlLang(): string {
  return currentLocale;
}
